package com.basic;

public class Switch {

	public static void main(String[] args) {
		
		int a=1;
		switch (a) {
		case 1:
			System.out.println(1);
		case 2:
			System.out.println(2);
		case 3:
			System.out.println(3);
			break;

		default:
			System.out.println(4);
			break;
		}
	}
}
