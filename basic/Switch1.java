package com.basic;

public class Switch1 {

	public static void main(String[] args) {
      char a='a';
		switch (a) {
		case 'a':
		case 'e':
		case 'i':
		case 'o':
		case 'u':System.out.println("its a vowel");
			break;
			
			default:
				System.out.println("not a vowel");
				break;
				
			
		}
}
}
